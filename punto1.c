/*Ingresar tres números enteros, presentar por pantalla la suma de ellos */
#include <stdio.h>

int A, B, C;

int main() {
    printf("Ingrese 3 números:\n");
    scanf("%d", &B);
    scanf("%d", &C);
    scanf("%d", &A);

    printf("La suma de los tres números es: %d\n", A + B + C);

    return 0;
}
